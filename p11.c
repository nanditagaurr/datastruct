#include<stdio.h>
//************************************************************
//*******************main function****************************
void main()
{
    int a[]={2,10,50,60},b[]={2,50,55,75},c[20],i,k,j,sc;
    int sa=4;
    int sb=4;
    printf("\narray 1:");
    for(i=0;i<sa;i++)
        printf("  %d",a[i]);
    printf("\narray 2:");
    for(i=0;i<sb;i++)
        printf("  %d",b[i]);
    for(i=0;i<sa;i++)
        c[i]=a[i];
    sc=sa;
    for(i=0,k=0;i<sb;k++)
    {
        if(c[k]<=b[i]&&b[i]<c[k+1])
        {
            for(j=sc;j>k;j--)
            {
                c[j]=c[j-1];
            }
            sc++;
            c[k+1]=b[i];
            i++;
        }
    }
    printf("\nmerged array is:");
    for(i=0;i<sc;i++)
        printf("%d   ",c[i]);
}

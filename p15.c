//*******************main function****************************

void main()
{
    int a[]={2,10,50,60},b[]={2,50,55,75},c[10],i,j,k;
    printf("\narray 1:");
    for(i=0;i<4;i++)
        printf("  %d",a[i]);
    printf("\narray 2:");
    for(i=0;i<4;i++)
        printf("  %d",b[i]);

    k=0;
    for(i=0;i<4;i++)
        for(j=0;j<4;j++)
        if(a[i]==b[j])
           {
               c[k]=a[i];
               k++;
           }
    printf("\nset intersection:");
    for(i=0;i<k;i++)
        printf("%d  ",c[i]);
}

#include<stdio.h>
//************************************************************
//*******************main function****************************

void main()
{
    int a[]={2,10,50,60},b[]={2,50,55,75},c[10],i=0,k=0,j=0,p;
    printf("\narray 1:");
    for(i=0;i<4;i++)
        printf("  %d",a[i]);
    printf("\narray 2:");
    for(i=0;i<4;i++)
        printf("  %d",b[i]);
    i=0;
    while(i<4)
    {
        c[k]=a[i];
        k++;
        i++;
    }
    for(i=0,p=0;i<4;p++)
    {
        if(c[p]<=b[i]&&b[i]<c[p+1])
        {
            for(j=k;j>p;j--)
            {
                c[j]=c[j-1];
            }
            k++;
            c[p+1]=b[i];
            i++;
        }
    }
    for(i=0;i<k;i++)
    {
        if(c[i]==c[i+1])
        {
            for(j=i;j<k;j++)
                c[j]=c[j+1];
            k--;

        }
    }
    printf("\nunion:");
    for(i=0;i<k;i++)
        printf("%d  ",c[i]);

}
